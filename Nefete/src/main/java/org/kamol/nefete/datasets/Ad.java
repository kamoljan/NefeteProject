package org.kamol.nefete.datasets;

public class Ad {
    private String title;
    private String description;
    private int currency;
    private String price;
    private int category;
    private String newborn1;
    private String newborn2;
    private String newborn3;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getCurrency() {
        return currency;
    }

    public void setCurrency(int currency) {
        this.currency = currency;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public int getCategory() {
        return category;
    }

    public void setCategory(int category) {
        this.category = category;
    }

    public String getNewborn1() {
        return newborn1;
    }

    public void setNewborn1(String newborn1) {
        this.newborn1 = newborn1;
    }

    public String getNewborn2() {
        return newborn2;
    }

    public void setNewborn2(String newborn2) {
        this.newborn2 = newborn2;
    }

    public String getNewborn3() {
        return newborn3;
    }

    public void setNewborn3(String newborn3) {
        this.newborn3 = newborn3;
    }
}

